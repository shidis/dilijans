<p align="center"><strong>7.&nbsp; Ответственность Сторон</strong></p>
<p>7.1. Стороны несут имущественную ответственность за неисполнение обязательств, предусмотренных настоящим Договором, в соответствии с положениями настоящего Договора и действующим законодательством Российской Федерации.</p>
<p>7.2. За необоснованный отказ от получения оплаченного Товара Покупатель в десятидневный срок уплачивает Продавцу штраф&nbsp; в размере 10 %&nbsp; от стоимости непринятого Товара.</p>
<p>7.3. Стороны обязуются незамедлительно письменно информировать друг друга о возникающих трудностях, которые могут привести к невыполнению настоящего договора или отдельных его условий.</p>
