<p align="center"><strong>8.&nbsp; Форс-мажор (действие непреодолимой силы)</strong></p>
<p>8.1. Ни одна из Сторон не несет ответственности перед другой Стороной за частичное или полное невыполнение обязательств по настоящему Договору, обусловленное обстоятельствами, возникшими помимо воли и желания Сторон и которые нельзя предвидеть или избежать, включая объявленную или фактическую войну, гражданские волнения, эпидемии, блокаду, эмбарго, землетрясения, наводнения, пожары и другие стихийные бедствия.</p>
<p>8.2. При наступлении обстоятельств, указанных в п.8.1. каждая сторона должна в течение десяти суток, известить о них в письменном виде другую сторону. Извещение должно содержать данные о характере обстоятельств, а также официальные документы, удостоверяющие наличие этих обстоятельств.</p>


