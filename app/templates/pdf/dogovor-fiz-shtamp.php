<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.2. Риск случайной гибели или случайной порчи, утраты или повреждения Товара, являющегося предметом настоящего Договора, несет Продавец или Покупатель в зависимости от того, кто из них обладал правом собственности на Товар в момент случайной гибели или случайной&nbsp; ее порчи.</p>
