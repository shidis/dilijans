<p align="center"><strong>2. Цена&nbsp; и качество Товара</strong></p>

<p>2.1. Цена Договора определяется Счетом к настоящему Договору.</p>
<p>2.2 Изменение цен и номенклатуры Товара&nbsp; подлежит&nbsp; письменному согласованию Сторонами.</p>
<p>2.3 &nbsp;Цена единицы Товара включает стоимость товара, упаковки.&nbsp;&nbsp;</p>
<p>2.4. Поставляемый Товар по качеству должен соответствовать действующим ГОСТам и &nbsp;техническим условиям (ТУ) на данный вид Товара.</p>
