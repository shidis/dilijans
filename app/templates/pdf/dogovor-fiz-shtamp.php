<p align="center"><strong>3. Условия оплаты и&nbsp; порядок расчетов</strong></p>
<p>3.1. Товар оплачивается&nbsp; Покупателем путем 100%-й предоплаты на основании выставленного Продавцом счета в течении 3-х рабочих дней. Форма оплаты товара &ndash; перечисление денежных средств по реквизитам Продавца, указанным в данном Договоре.</p>
<p>3.2. Оплата производится в рублях РФ. Датой оплаты считается дата зачисления денежных средств на расчетный счет Продавца.</p>
